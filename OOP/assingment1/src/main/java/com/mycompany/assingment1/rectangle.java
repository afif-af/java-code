
package com.mycompany.assingment1;
public class rectangle {
    private double l;
    private double b;
    public rectangle(double l,double b)
    {
        this.l=l;
        this.b=b;
    }
    public double Area()
    {
        return l*b;
    }
    public double pe()
    {
        return 2*(l+b);
    }
    
    
}
